public class CiclodoWhile {
    public static void main(String[] args) {
        int i = 2; // empezamos en 2, el primer número par

        // Ciclo while que se ejecuta mientras i sea menor o igual a 100
        while (i <= 100) {
            System.out.println(i); // imprimir el número par
            i += 2; // pasar al siguiente número par
        }
    }
}

    

